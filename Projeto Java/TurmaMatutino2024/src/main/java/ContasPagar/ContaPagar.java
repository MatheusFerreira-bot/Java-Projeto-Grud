package ContasPagar;

public class ContaPagar {
    
    private int CodContaPagar;
    private String DescricaoConta;
    private float Preco;
    private String DataVencimento;
    private String DataPagamento;
    private String Fornecedor;
    private String Categoria;
    private String StatusPag;
    private String MetodoPag;
    private String Observacao;

    public int getCodContaPagar() {
        return CodContaPagar;
    }

    public void setCodContaPagar(int CodContaPagar) {
        this.CodContaPagar = CodContaPagar;
    }

    public String getDescricaoConta() {
        return DescricaoConta;
    }

    public void setDescricaoConta(String DescricaoConta) {
        this.DescricaoConta = DescricaoConta;
    }

    public float getPreco() {
        return Preco;
    }

    public void setPreco(float Preco) {
        this.Preco = Preco;
    }

    public String getDataVencimento() {
        return DataVencimento;
    }

    public void setDataVencimento(String DataVencimento) {
        this.DataVencimento = DataVencimento;
    }

    public String getDataPagamento() {
        return DataPagamento;
    }

    public void setDataPagamento(String DataPagamento) {
        this.DataPagamento = DataPagamento;
    }

    public String getFornecedor() {
        return Fornecedor;
    }

    public void setFornecedor(String Fornecedor) {
        this.Fornecedor = Fornecedor;
    }

    public String getCategoria() {
        return Categoria;
    }

    public void setCategoria(String Categoria) {
        this.Categoria = Categoria;
    }

    public String getStatusPag() {
        return StatusPag;
    }

    public void setStatusPag(String StatusPag) {
        this.StatusPag = StatusPag;
    }

    public String getMetodoPag() {
        return MetodoPag;
    }

    public void setMetodoPag(String MetodoPag) {
        this.MetodoPag = MetodoPag;
    }

    public String getObservacao() {
        return Observacao;
    }

    public void setObservacao(String Observacao) {
        this.Observacao = Observacao;
    }
    
    
    
}
