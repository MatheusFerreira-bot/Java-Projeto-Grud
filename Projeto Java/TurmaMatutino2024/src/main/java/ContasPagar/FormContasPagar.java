    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        ContaPagar cp = this.MontarContaPagar();
        if(cp != null){
            ContaPagarCtrl cpc = new ContaPagarCtrl();
            cpc.insert(cp);            
        }else{
            JOptionPane.showMessageDialog(null,"Favor preencher os campos corretamente.");
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        ContaPagar cp = this.MontarContaPagar();
        if(cp != null && !txtfCodigoConta.getText().trim().isEmpty()){
            cp.setCodContaPagar(Integer.parseInt(txtfCodigoConta.getText()));
            ContaPagarCtrl cpc = new ContaPagarCtrl();
            cpc.update(cp);
        }else{
            JOptionPane.showMessageDialog(null,"Favor preencher os campos corretamente.");            
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ContaPagarCtrl cpc = new ContaPagarCtrl();
        if (!txtfCodigoConta.getText().trim().isEmpty()){
            int cod = Integer.parseInt(txtfCodigoConta.getText());
            cpc.delete(cod);
        }else{
            JOptionPane.showMessageDialog(null,"Preencha o campo.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

