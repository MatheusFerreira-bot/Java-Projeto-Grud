    public ContaPagar MontarContaPagar(){
        if (TxtaDesc.getText().trim().isEmpty() || TxtfPreco.getText().trim().isEmpty()
                || TxtfDataVenc.getText().trim().isEmpty() || TxtfDataPag.getText().trim().isEmpty()
                || TxtfFornecedor.getText().trim().isEmpty() || TxtfCategoria.getText().trim().isEmpty()
                || TxtfStatus.getText().trim().isEmpty() || TxtfMetodoPag.getText().trim().isEmpty()
                || TxtaObservacao.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Favor preencher os campos corretamente.");
            return null;
        }try{
            ContaPagar cp = new ContaPagar();
            cp.setDescricaoConta(TxtaDesc.getText());
            cp.setPreco(Float.parseFloat(TxtfPreco.getText()));
            cp.setDataVencimento(TxtfDataVenc.getText());
            cp.setDataPagamento(TxtfDataPag.getText());
            cp.setFornecedor(TxtfFornecedor.getText());
            cp.setCategoria(TxtfCategoria.getText());
            cp.setStatusPag(TxtfStatus.getText());
            cp.setMetodoPag(TxtfMetodoPag.getText());
            cp.setObservacao(TxtaObservacao.getText());
            return cp;            
        }catch(NumberFormatException n){
            JOptionPane.showMessageDialog(null,n,"Error",JOptionPane.ERROR_MESSAGE);
            return null;
        }
    }
    
