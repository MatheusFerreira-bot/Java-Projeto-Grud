package Livros;

public class Livros {
    
    private int CodLivros;
    private String Titulo;
    private String Autor;
    private long ISBN;
    private String Editora;
    private String AnoPub;
    private String Genero;
    private int NumPag;
    private String Idioma;
    private String LocBiblioteca;

    public int getCodLivros() {
        return CodLivros;
    }

    public void setCodLivros(int CodLivros) {
        this.CodLivros = CodLivros;
    }

    public String getTitulo() {
        return Titulo;
    }

    public void setTitulo(String Titulo) {
        this.Titulo = Titulo;
    }

    public String getAutor() {
        return Autor;
    }

    public void setAutor(String Autor) {
        this.Autor = Autor;
    }

    public long getISBN() {
        return ISBN;
    }

    public void setISBN(long ISBN) {
        this.ISBN = ISBN;
    }

    public String getEditora() {
        return Editora;
    }

    public void setEditora(String Editora) {
        this.Editora = Editora;
    }

    public String getAnoPub() {
        return AnoPub;
    }

    public void setAnoPub(String AnoPub) {
        this.AnoPub = AnoPub;
    }

    public String getGenero() {
        return Genero;
    }

    public void setGenero(String Genero) {
        this.Genero = Genero;
    }

    public int getNumPag() {
        return NumPag;
    }

    public void setNumPag(int NumPag) {
        this.NumPag = NumPag;
    }

    public String getIdioma() {
        return Idioma;
    }

    public void setIdioma(String Idioma) {
        this.Idioma = Idioma;
    }

    public String getLocBiblioteca() {
        return LocBiblioteca;
    }

    public void setLocBiblioteca(String LocBiblioteca) {
        this.LocBiblioteca = LocBiblioteca;
    }
    
    
    
}
