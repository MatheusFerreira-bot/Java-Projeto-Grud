    public Produtos MontarProduto(){
        if(TxtfNomeProduto.getText().trim().isEmpty() || TxtfCodigoProduto.getText().trim().isEmpty()
                || TxtfDescricao.getText().trim().isEmpty() || TxtfPreco.getText().trim().isEmpty()
                || TxtfQuantEstoque.getText().trim().isEmpty() || TxtfCategoria.getText().trim().isEmpty()
                || TxtfDataValid.getText().trim().isEmpty() || TxtfFornecedor.getText().trim().isEmpty()
                || TxtfLocEstoque.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Favor preencher todos os campos.");
            return null;
        }
        try{
            Produtos p = new Produtos();
            p.setNomeProduto(TxtfNomeProduto.getText());
            p.setCodigoProduto(TxtfCodigoProduto.getText());
            p.setDescricao(TxtfDescricao.getText());
            p.setPreco(Float.parseFloat(TxtfPreco.getText()));
            p.setQuantidadeEstoque(Float.parseFloat(TxtfQuantEstoque.getText()));
            p.setCategoria(TxtfCategoria.getText());
            p.setDataValidade(TxtfDataValid.getText());
            p.setFornecedor(TxtfFornecedor.getText());
            p.setLocEstoque(TxtfLocEstoque.getText());
            return p;
        }catch(NumberFormatException n){
            JOptionPane.showMessageDialog(null,n,"Error",JOptionPane.ERROR_MESSAGE);
            return null;
        }    
    }
    
