    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
       Produtos p = this.MontarProduto();
       if (p != null){
           ProdutosCtrl pc = new ProdutosCtrl();
           pc.insert(p);
       }else{
           JOptionPane.showMessageDialog(null,"Favor preencher todos os campos.");
       }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
       Produtos p = this.MontarProduto();
       if (p != null && !txtfCodProduto.getText().trim().isEmpty()){
           p.setCodProduto(Integer.parseInt(txtfCodProduto.getText()));
           ProdutosCtrl pc = new ProdutosCtrl();
           pc.update(p);
       }else{
           JOptionPane.showMessageDialog(null,"Favor preencher todos os campos.");
       } 
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        ProdutosCtrl pc = new ProdutosCtrl();
        if (!txtfCodProduto.getText().trim().isEmpty()){
            int cod = Integer.parseInt(txtfCodProduto.getText());
            pc.delete(cod);
        }else{
            JOptionPane.showMessageDialog(null,"Preencha o campo.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

