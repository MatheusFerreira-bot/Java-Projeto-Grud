package Alunos;

public class Alunos {
    
    private int CodAlunos;
    private String Nome;
    private long CPF;
    private String Endereco;
    private long Telefone;
    private String Email;
    private String DataNasc;
    private int SerieAno;
    private String NomeResponsavel;
    private String EscolaOrigem;

    public int getCodAlunos() {
        return CodAlunos;
    }

    public void setCodAlunos(int CodAlunos) {
        this.CodAlunos = CodAlunos;
    }
    
    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    public long getCPF() {
        return CPF;
    }

    public void setCPF(long CPF) {
        this.CPF = CPF;
    }

    public String getEndereco() {
        return Endereco;
    }

    public void setEndereco(String Endereco) {
        this.Endereco = Endereco;
    }

    public long getTelefone() {
        return Telefone;
    }

    public void setTelefone(long Telefone) {
        this.Telefone = Telefone;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getDataNasc() {
        return DataNasc;
    }

    public void setDataNasc(String DataNasc) {
        this.DataNasc = DataNasc;
    }

    public int getSerieAno() {
        return SerieAno;
    }

    public void setSerieAno(int SerieAno) {
        this.SerieAno = SerieAno;
    }

    public String getNomeResponsavel() {
        return NomeResponsavel;
    }

    public void setNomeResponsavel(String NomeResponsavel) {
        this.NomeResponsavel = NomeResponsavel;
    }

    public String getEscolaOrigem() {
        return EscolaOrigem;
    }

    public void setEscolaOrigem(String EscolaOrigem) {
        this.EscolaOrigem = EscolaOrigem;
    }
    
    
    
}
